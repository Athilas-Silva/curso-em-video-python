n1 = int(input("Informe um número: "))
dobro = n1 * 2
tri = n1 * 3
raiz = n1 ** (1/2)
# outro metodo para a raiz quadrada é math.sqrt() E para deixar em duas casas decimais use (:.2)
print("O dobro de {} é {}, \n o triplo de {} é {}, \n a raiz de {} é {:.2}".format(n1, dobro, n1, tri, n1, raiz))

